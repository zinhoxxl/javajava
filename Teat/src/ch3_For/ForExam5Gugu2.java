package ch3_For;

public class ForExam5Gugu2 {
	// 이중반복형태의 for문
	public static void main(String[] args) {

		int i;
		for (i = 1; i <= 9; i++) {
			for (int j = 1; j <= 9; j++) {
				System.out.print(i + "*" + j + "=" + i * j + "\t");
			}
			System.out.println(); // 한줄 밑으로 내리는 명령문;
		}

		System.out.println("i=" + i);
	}

}
